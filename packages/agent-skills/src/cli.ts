/**
 * List available integrations
 */
function listIntegrations() {
  console.log('Available integrations:\n');
  console.log('  stripe   - Stripe payment webhooks');
  console.log('  github   - GitHub repository webhooks');
  console.log('\nMore integrations coming soon: shopify, slack, twilio, sendgrid');
}

/**
 * Initialize an integration
 */
function initIntegration(integration: string, outputDir: string) {
  const template = getTemplate(integration);

  if (!template) {
    console.error(`Error: Unknown integration "${integration}"`);
    console.error('Run "npx @hookwing/agent-skills list" to see available integrations');
    process.exit(1);
  }

  const outputPath = join(outputDir, template.filename);

  // Check if file already exists
  if (existsSync(outputPath)) {
    console.error(`Error: File already exists: ${outputPath}`);
    console.error('Use --force to overwrite');
    process.exit(1);
  }

  // Create output directory if needed
  if (!existsSync(outputDir)) {
    mkdirSync(outputDir, { recursive: true });
  }

  writeFileSync(outputPath, template.content);

  console.log(`Created ${template.filename} in ${outputDir}/`);
  console.log('\nNext steps:');
  console.log('1. Set up your webhook secret in environment variables');
  console.log('2. Import and use the handler in your app');
  console.log(`3. Deploy and configure the webhook URL in ${integration} dashboard`);
}

/**
 * Main CLI entry point
 */
function main() {
  const args = parseArgs();

  switch (args.command) {
    case 'list':
      listIntegrations();
      break;

    case 'init':
      if (!args.integration || !args.outputDir) {
        console.error('Error: integration and output-dir required');
        process.exit(1);
      }
      initIntegration(args.integration, args.outputDir);
      break;

    default:
      printHelp();
  }
}

main();
