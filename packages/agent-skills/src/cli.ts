#!/usr/bin/env node

/**
 * CLI for @hookwing/agent-skills
 *
 * Usage:
 *   npx @hookwing/agent-skills init stripe
 *   npx @hookwing/agent-skills init github
 *   npx @hookwing/agent-skills list
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

interface CliArgs {
  command: string;
  integration?: string;
  outputDir?: string;
}

/**
 * Parse CLI arguments
 */
function parseArgs(): CliArgs {
  const args = process.argv.slice(2);

  if (args.length === 0) {
    printHelp();
    process.exit(0);
  }

  const command = args[0];

  if (command === 'help' || command === '--help' || command === '-h') {
    printHelp();
    process.exit(0);
  }

  if (command === 'list' || command === 'ls') {
    return { command: 'list' };
  }

  if (command === 'init') {
    const integration = args[1];
    const outputDir = args[2] || '.';

    if (!integration) {
      console.error('Error: integration name required');
      console.error('Usage: npx @hookwing/agent-skills init <integration> [output-dir]');
      console.error('Example: npx @hookwing/agent-skills init stripe ./webhooks');
      process.exit(1);
    }

    return { command: 'init', integration, outputDir };
  }

  console.error(`Unknown command: ${command}`);
  printHelp();
  process.exit(1);
}

/**
 * Print help message
 */
function printHelp() {
  console.log(`
@hookwing/agent-skills CLI

Usage:
  npx @hookwing/agent-skills init <integration> [output-dir]
  npx @hookwing/agent-skills list

Commands:
  init <integration>    Scaffold a webhook handler for the integration
  list                 List available integrations

Examples:
  npx @hookwing/agent-skills init stripe           # Create stripe handler in current dir
  npx @hookwing/agent-skills init github ./src     # Create github handler in ./src

Available Integrations:
  stripe   - Stripe payment webhooks
  github   - GitHub repository webhooks
`);
}

